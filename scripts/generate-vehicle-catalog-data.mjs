import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const ROUTE_FILE = join(ROOT, 'src/app/(frontend)/next/import-vehicles/route.ts')
const OUT_FILE = join(ROOT, 'src/lib/vehicle-seed/vehicle-catalog-data.ts')

function extractBalancedArray(source, openIndex) {
  let depth = 0
  let inString = false
  let stringChar = ''
  let escaped = false

  for (let i = openIndex; i < source.length; i++) {
    const ch = source[i]

    if (inString) {
      if (escaped) {
        escaped = false
        continue
      }
      if (ch === '\\') {
        escaped = true
        continue
      }
      if (ch === stringChar) inString = false
      continue
    }

    if (ch === '"' || ch === "'" || ch === '`') {
      inString = true
      stringChar = ch
      continue
    }

    if (ch === '[') depth++
    else if (ch === ']') {
      depth--
      if (depth === 0) return source.slice(openIndex, i + 1)
    }
  }

  throw new Error('Unbalanced array brackets in VEHICLE_DATA')
}

function extractLegacyVehicleData(source) {
  const startMarker = 'const VEHICLE_DATA: VehicleDef[] = '
  const start = source.indexOf(startMarker)
  if (start === -1) throw new Error('Could not find VEHICLE_DATA start marker')

  const valueStart = start + startMarker.length
  const arrayOpen = source.indexOf('[', valueStart)
  if (arrayOpen === -1) throw new Error('Could not find VEHICLE_DATA array open bracket')

  const arrayLiteral = extractBalancedArray(source, arrayOpen)

  return Function(`"use strict"; return (${arrayLiteral})`)()
}

function serializeValue(value, indent = 0) {
  const pad = '  '.repeat(indent)
  const padInner = '  '.repeat(indent + 1)

  if (value === null) return 'null'
  if (typeof value === 'string') return JSON.stringify(value)
  if (typeof value === 'number' || typeof value === 'boolean') return String(value)

  if (Array.isArray(value)) {
    if (value.length === 0) return '[]'
    const items = value.map((item) => `${padInner}${serializeValue(item, indent + 1)}`)
    return `[\n${items.join(',\n')},\n${pad}]`
  }

  if (typeof value === 'object') {
    const entries = Object.entries(value).filter(([, v]) => v !== undefined)
    if (entries.length === 0) return '{}'
    const lines = entries.map(([key, v]) => `${padInner}${key}: ${serializeValue(v, indent + 1)}`)
    return `{\n${lines.join(',\n')},\n${pad}}`
  }

  throw new Error(`Unsupported value type: ${typeof value}`)
}

async function main() {
  const routeSource = readFileSync(ROUTE_FILE, 'utf8')
  const legacyData = extractLegacyVehicleData(routeSource)

  const buildModule = await import(
    pathToFileURL(join(ROOT, 'src/lib/vehicle-seed/buildThreeTierCatalog.ts')).href
  )
  const catalog = buildModule.buildThreeTierCatalog(legacyData)

  const categoryBlock = `export const CATEGORY_DATA = [
  { title: 'Bakkies', slug: 'bakkies', sortOrder: 1 },
  { title: 'SUVs', slug: 'suvs', sortOrder: 2 },
  { title: 'Passenger Cars', slug: 'passenger-cars', sortOrder: 3 },
  { title: 'Vans & Buses', slug: 'vans-and-buses', sortOrder: 4 },
] as const satisfies import('./vehicle-catalog-types').CategoryDef[]
`

  const output = `import type { VehicleDef } from './vehicle-catalog-types'

/**
 * Native 3-tier vehicle catalog seed data (Vehicle → Model → Variant).
 * Generated by scripts/generate-vehicle-catalog-data.mjs — do not hand-edit structure;
 * regenerate from legacy flat data if the transform logic changes.
 */
export const VEHICLE_CATALOG_DATA: VehicleDef[] = ${serializeValue(catalog)}

${categoryBlock}`

  writeFileSync(OUT_FILE, output, 'utf8')

  const vehicleCount = catalog.length
  const modelCount = catalog.reduce((sum, v) => sum + v.models.length, 0)
  const variantCount = catalog.reduce(
    (sum, v) => sum + v.models.reduce((ms, m) => ms + m.variants.length, 0),
    0,
  )

  console.log(`Wrote ${OUT_FILE}`)
  console.log(`  ${vehicleCount} vehicles, ${modelCount} models, ${variantCount} variants`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
